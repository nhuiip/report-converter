     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $mapping = TeamMapping::where('teamId', $request->teamId)->where('userId', $request->userId)->first();
        if ($mapping == null) {
            $data = new TeamMapping($request->all());
            $data->save();
        }
        return true;
    }

